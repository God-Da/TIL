# 문제 설명
# 다음과 같이 출력하도록 코드를 작성해 주세요.
# 출력 예시 !@#$%^&*(\'"<>?:;

# 🛠 접근 방식
# 1. 출력해야 하는 문자열에 **특수문자들이 포함**되어 있음.
# 2. 특히 `\`(백슬래시), `'`(작은따옴표), `"`(큰따옴표) 같은 문자들은 **이스케이프 처리**가 필요함.
# 3. `\`를 활용하여 특수문자를 올바르게 출력해야 함.
# 4. 또는 **raw 문자열(`r'...'`)을 활용하면 백슬래시를 그대로 출력 가능**.

# 내 풀이
print('!@#$%^&*(\\\'\"<>?:;')

# ✨ 오답노트
# 1. 백슬래시(`\`)를 사용해 특수문자 `'`와 `"`를 올바르게 출력했음.
# 2. 하지만 **raw 문자열(`r'...'`)을 활용하면 더 간결하게 코드 작성 가능**.
# 3. 즉, **이스케이프 문자를 하나하나 추가할 필요 없이 `r'...'`를 사용하면 편리함**.

# 다른사람의 풀이
# 1
print(r'!@#$%^&*(\'"<>?:;')

# 🔍 배운 점
# - Python에서는 **백슬래시(`\`)를 사용해 특수문자를 이스케이프 처리**할 수 있음.
# - 하지만 **raw 문자열(`r'...'`)을 사용하면 더 쉽게 백슬래시를 그대로 출력 가능**.
# - `r'...'`을 활용하면 **이스케이프 문자를 추가할 필요 없이 간단한 코드 작성 가능**!