# 문제 설명
# 길이가 같은 두 문자열 str1과 str2가 주어집니다.
# 두 문자열의 각 문자가 앞에서부터 서로 번갈아가면서 한 번씩 등장하는 문자열을 만들어 return 하는 solution 함수를 완성해 주세요.
# 입출력 예
# str1	str2	result
# "aaaaa"	"bbbbb"	"ababababab"

# 🛠 접근 방식
# 1. 두 문자열의 각 문자를 번갈아가면서 이어 붙이는 것이 핵심.
# 2. 반복문을 활용하여 각 인덱스의 문자를 차례로 꺼내 새로운 문자열에 추가.
# 3. 리스트를 활용해 문자를 저장한 후 `''.join()`을 사용하여 문자열로 변환.

# 내 풀이
def solution(str1, str2):
    result = []  # 번갈아 추가할 문자를 저장할 리스트
    
    for i in range(len(str1)):  # 문자열 길이만큼 반복
        result.append(str1[i])  # 첫 번째 문자열의 i번째 문자 추가
        result.append(str2[i])  # 두 번째 문자열의 i번째 문자 추가
        
    return ''.join(result)  # 리스트를 문자열로 변환 후 반환

# ✨ 오답노트
# 1. 리스트를 사용하여 문자열을 생성했는데, 리스트 대신 문자열을 직접 생성하는 방식도 가능함.
# 2. 문자열 결합 연산(`+`)을 활용하면 `join()` 없이도 깔끔한 코드 작성 가능.
# 3. `zip()`을 사용하면 더 간결하게 두 문자열을 순회하며 처리 가능.

