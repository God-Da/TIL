# 문제 설명
# 영어 알파벳으로 이루어진 문자열 str이 주어집니다.
# 각 알파벳을 대문자는 소문자로 소문자는 대문자로 변환해서 출력하는 코드를 작성해 보세요.
# 입력  aBcDeFg
# 출력  AbCdEfG

# 🛠 접근 방식
# 1. 문자열을 순회하면서 각 문자(char)가 **소문자인지, 대문자인지 확인**해야 함.
# 2. `islower()`를 사용하여 **소문자라면 대문자로 변환**, 반대로 **대문자는 소문자로 변환**.
# 3. 변환된 문자들을 새로운 문자열에 저장하고 마지막에 출력.

# 내 풀이
str = input()
result=""
for char in str:
    if(char.islower()):
        result += char.upper()
    else:
        result += char.lower()
print(result)

# ✨ 오답노트
# 1. `result +=`을 사용하면 문자열이 새로 생성되는 방식이라 **성능이 저하될 가능성이 있음**.
#    - 문자열을 직접 변경하는 것이 아니라 새 문자열을 생성하면서 메모리를 더 많이 사용하게 됨.
# 2. **리스트를 활용해서 문자열을 처리**하는 것이 더 효율적일 수 있음.
# 3. `swapcase()` 같은 **내장 함수**를 사용하면 더 간결한 해결 가능.

# 다른 사람의 풀이
# 1 파이썬 내장 메서드 활용 (가장 간결)
print(input().swapcase())

# 2  리스트 컴프리헨션 활용 (성능 최적화)
print(''.join(x.upper() if x == x.lower() else x.lower() for x in input())) #리스트컴프리헨션인가?

# 🔍 배운 점
# - `swapcase()`를 활용하면 **더 간결하고 직관적으로 문제 해결 가능**.
# - **리스트 컴프리헨션을 사용하면 코드가 짧고 실행 속도가 더 빠름**.
# - 문자열을 조작할 때 `+=` 대신 **리스트를 사용하고 `join()`을 활용하는 것이 더 효율적**.
